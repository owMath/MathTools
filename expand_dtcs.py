def main():
    print(f"Lendo arquivo: {INPUT_FILE}")
    with open(INPUT_FILE, "r", encoding="utf-8") as f:
        existing = json.load(f)

    original_count = len(existing)
    print(f"DTCs existentes: {original_count}")

    existing_codes = {item["code"] for item in existing}

    oem_dtcs = build_oem_dtcs()
    total_generated = len(oem_dtcs)
    print(f"DTCs OEM gerados: {total_generated}")

    added = 0
    skipped = 0
    by_manufacturer = {}

    for entry in oem_dtcs:
        manufacturer = entry["system"].split(" - ")[-1].replace(" Enhanced", "")
        if entry["code"] not in existing_codes:
            existing.append(entry)
            existing_codes.add(entry["code"])
            added += 1
            by_manufacturer[manufacturer] = by_manufacturer.get(manufacturer, 0) + 1
        else:
            skipped += 1

    existing.sort(key=lambda x: x["code"])

    final_count = len(existing)
    print(f"\nEscrevendo {final_count} DTCs no arquivo...")
    with open(INPUT_FILE, "w", encoding="utf-8") as f:
        json.dump(existing, f, indent=1, ensure_ascii=False)

    print("\n" + "=" * 50)
    print("RESUMO DA EXPANSÃO DE DTCs")
    print("=" * 50)
    print(f"DTCs originais:     {original_count}")
    print(f"DTCs OEM gerados:   {total_generated}")
    print(f"DTCs adicionados:   {added}")
    print(f"DTCs duplicados:    {skipped}")
    print(f"Total final:        {final_count}")
    print(f"\nPor fabricante:")
    for mfr, count in sorted(by_manufacturer.items()):
        print(f"  {mfr:20s}: +{count} códigos")
    print("=" * 50)
    print("Concluído com sucesso!")


if __name__ == "__main__":
    main()
