def get_system(code, manufacturer):
    num = int(code[1:])
    if 1000 <= num <= 1199:
        return f"Sensores - {manufacturer} Enhanced"
    elif 1200 <= num <= 1399:
        return f"Combustível/Ignição - {manufacturer} Enhanced"
    elif 1400 <= num <= 1499:
        return f"Emissões - {manufacturer} Enhanced"
    elif 1500 <= num <= 1599:
        return f"Controle de Marcha Lenta - {manufacturer} Enhanced"
    elif 1600 <= num <= 1699:
        return f"ECU/PCM - {manufacturer} Enhanced"
    elif 1700 <= num <= 1799:
        return f"Transmissão - {manufacturer} Enhanced"
    return f"Motor (Powertrain) - {manufacturer} Enhanced"


def get_severity(code, desc):
    desc_lower = desc.lower()
    high_keywords = [
        "over temperature", "over-temperature", "protection active",
        "immobiliz", "theft", "shutdown", "crash", "disabled",
        "engine stall", "over pressure", "battery disconnect",
        "oil pressure too low", "coolant over", "rpm.*limiter",
        "forced engine shutdown", "mechanically failed",
    ]
    low_keywords = [
        "readiness", "not complete", "learning not complete",
        "snapshot", "data available", "reprogramm", "not programmed",
        "mileage not stored", "eeprom", "coding", "calibration",
        "idle learn", "adaptive clip", "monitor not enabled",
    ]
    for kw in high_keywords:
        if kw in desc_lower:
            return "high"
    for kw in low_keywords:
        if kw in desc_lower:
            return "low"
    return "medium"


def make_entry(code, desc_en, manufacturer):
    return {
        "code": code,
        "category": "P",
        "system": get_system(code, manufacturer),
        "description": desc_en,
        "description_en": desc_en,
        "description_pt": "",
        "severity": get_severity(code, desc_en),
    }


